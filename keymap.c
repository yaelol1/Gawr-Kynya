bool encoder_update_user(uint8_t index, bool clockwise) {
  
        switch (biton32(layer_state)) {
            case _RAISE:
                // Scrolling with PageUp and PgDn.
                if (clockwise) {
                    tap_code(KC_MPRV);
                } else {
                    tap_code(KC_MNXT);
                }
                break;
            case _LOWER:
                // Scrolling with PageUp and PgDn.
                if (clockwise) {
                    tap_code(KC_DOWN);
                } else {
                    tap_code(KC_UP);
                }
                break;
            case _GAMING:
                // Scrolling with PageUp and PgDn.
                if (clockwise) {
                    tap_code(KC_LEFT);
                } else {
                    tap_code(KC_RIGHT);
                }
                break;
            default:
                // Volume control.
                if (clockwise) {
                    tap_code(KC_VOLD);
                } else {
                    tap_code(KC_VOLU);
                }
                break;
        }
    
    return true;
}

